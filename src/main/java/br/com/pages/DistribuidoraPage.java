package br.com.pages;

public class DistribuidoraPage {
	private String cnpj;	
	private String estado;
	private String submercado;	
	private String horario_inicio_ponta;	
	private String horario_final_ponta;	
	private String horario_inicio_reativo;	
	private String horario_final_reativo;	
	private String contatos;
	private String  nome;	
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getSubmercado() {
		return submercado;
	}
	public void setSubmercado(String submercado) {
		this.submercado = submercado;
	}
	public String getHorario_inicio_ponta() {
		return horario_inicio_ponta;
	}
	public void setHorario_inicio_ponta(String horario_inicio_ponta) {
		this.horario_inicio_ponta = horario_inicio_ponta;
	}
	public String getHorario_final_ponta() {
		return horario_final_ponta;
	}
	public void setHorario_final_ponta(String horario_final_ponta) {
		this.horario_final_ponta = horario_final_ponta;
	}
	public String getHorario_inicio_reativo() {
		return horario_inicio_reativo;
	}
	public void setHorario_inicio_reativo(String horario_inicio_reativo) {
		this.horario_inicio_reativo = horario_inicio_reativo;
	}
	public String getHorario_final_reativo() {
		return horario_final_reativo;
	}
	public void setHorario_final_reativo(String horario_final_reativo) {
		this.horario_final_reativo = horario_final_reativo;
	}
	public String getContatos() {
		return contatos;
	}
	public void setContatos(String contatos) {
		this.contatos = contatos;
	}
	

}
